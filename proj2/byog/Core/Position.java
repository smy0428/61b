package byog.Core;

public class Position {
    /**
     * simple class to present the position with x and y
     */
    private int x;
    private int y;
    public Position(int pX, int pY) {
        x = pX;
        y = pY;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
